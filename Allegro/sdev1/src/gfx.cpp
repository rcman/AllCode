/*
	new map data is in hexadecimal notation to make it easy to read

	DECIMAL	| HEX	| DESC
	-------------------------------------------
	(0) 	| 0x00  | the solid brick tile
	-------------------------------------------
	(35) 	| 0x23  | the black empty space tile
	-------------------------------------------
	(3)		| 0x03  | the ladder tile
	-------------------------------------------

	Here is why:

	When the tile to be drawn is zero,
	and we take the value zero and divide it by ten,
	then we get a quotient of zero, and a remainder of zero.
	So, our solid brick tile's upper-left corner coordinates
	from the tileset are 0, 0 (You can check serwers.bmp to be sure)

	Now, like I said, the black empty space tile is thirty-five.
	When we take thirty-five and divide it by ten,
	we get a quotient of three, and a remainder of five.
	If we multiple the tile width by three, then we get an X coordinate
	of ninety-six, and multiplying the tile height by five, gives us
	one-hundred and sixty. Check the sewers.bmp at coordinate 96,160 and you
	will find the blank space tile.

	Lastly is the ladder tile which is three.
	Divide three by ten, and get a quotient of zero, and a remainder of three.
	Multiply zero by the tile width, and we get of course zero. Our X coordinate
	is zero, and multiply three by the tile height and we get ninety-six.
	Check the bitmap at 0, 96 and you will find the ladder tile.

*/


